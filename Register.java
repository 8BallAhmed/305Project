    private void checkDigit(String name) {
        char temp;
        for (int i = 0; i < name.length(); i++) {
            temp = name.charAt(i);
            if (Character.isDigit(temp)) {
                throw new isNumberException("Wrong input! Please enter the corresponding information.");
            }
        }
    }

    private int checkSSN(String ssn) {
        int temp = -1;
        if (ssn.length() > 6 && ssn.length() <= 7) {
            temp = Integer.parseInt(ssn);
        }
        return temp;
    }

    private String checkPassword(char[] pass) {
        String temp = "";
        for (int i = 0; i < pass.length; i++) {
            temp += pass[i];
        }
        if (temp.length() > 6 && temp.length() <= 12) {
            return temp;
        }
        return "try again";
    }

