    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {                                               
        try {
            // if null, inform the user
            // if information is repeated, check with the user
            String name = nameTextField.getText();
            checkDigit(name);
            String temp = ssnTextField.getText();
            int ssn = checkSSN(temp);// must check in database that SSN in not repeated, if it is inform the user an account exists with this SSN
            if(ssn == -1){
                showMessageDialog(null, "Wrong input! Your SSN should be 7 digits.");
            }
            int age = Integer.parseInt(ageTextField.getText());
            char[] pass = passwordField.getPassword();
            String password = checkPassword(pass);
            if(password.equalsIgnoreCase("try again")){
                showMessageDialog(null, "Wrong input! Please enter a password within (7 to 12) characters.");
            }
            boolean diabetes = false, obesity = false;
            if (diabetesYRadioButton.isSelected()) {
                diabetes = true;
            } else {
                diabetes = false;
            }
            if (obesityYRadioButton.isSelected()) {
                obesity = true;
            } else {
                obesity = false;
            }
            Appointee p = new Appointee(ssn, password, name, age, diabetes, obesity, false);
            ClientSide c = new ClientSide(p);
            System.out.println(c);
        } catch (isNumberException ee) {
            showMessageDialog(null, ee.getMessage());
        } catch (NumberFormatException e) {
            showMessageDialog(null, "Wrong input! Please enter the corresponding information.");
        } catch (NullPointerException n) {
            showMessageDialog(null, "Wrong input! Please enter the value.");
        }
    }                                              

